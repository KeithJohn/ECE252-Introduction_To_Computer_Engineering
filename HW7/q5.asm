.ORIG x3000
	LD R1, STRING_1
	LD R2, SPACE
	LD R6,CHAR
	AND R3, R3, #0
	LD R5, STRING_2
	
REPEAT 
	LDR R4, R1,#0
	BRz PERCENT
	ADD R7, R4, R2
	BRz EQUAL
	STR R4, R5, #0
	ADD R5, R5, #1
	ADD R1, R1,#1
	BR REPEAT
	
PERCENT
	LD R2, negONE
	ADD R3, R3, R2
	BRn FINISH
	STR R6, R5, #0
	ADD R5, R5, #1
	BR PERCENT
	
	
FINISH
	LD R6,NULL 
	STR R6, R5, #0
STOP HALT

EQUAL
	ADD R3, R3, #1
	ADD R1, R1, #1
	BR REPEAT

	SPACE .FILL xFFE0
	STRING_1 .FILL x5000
	STRING_2 .FILL x5100
	CHAR .FILL x0025
	NULL .FILL x0000
	negONE .FILL xFFFF
.END