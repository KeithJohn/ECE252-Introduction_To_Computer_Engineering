
.ORIG x3000
	LD R0,	Loc1
	AND R1, R1, #0
	LD R2, COUNTER
	LD R3, negONE
	ADD R0, R0,#0
	BRp CLS
	LD R1, ONE
	
CLS	ADD R2, R2, R3
	BRz DONE
	ADD R0, R0, R0
	ADD R0, R0, R1
	
	
	BR CLS
	
DONE ST, R0, Loc2
STOP HALT
	
	ONE .FILL x0001
	negONE .FILL xFFFF
	Loc1 .BLKW 1
	Loc2 .FILL x4000
	COUNTER .FILL x0007
.END